def add_ruby_markup(text):
    """Add ruby markup to common kanji in Japanese text."""
    for kanji, reading in RUBY_PATTERNS.items():
        # Create ruby markup
        ruby_markup = f'<ruby><rb>{kanji}</rb><rp>(</rp><rt>{reading}</rt><rp>)</rp></ruby>'
        # Replace kanji with ruby markup
        text = text.replace(kanji, ruby_markup)
    
    return text

def convert_verse_to_ruby(verse_text):
    """Convert a single verse to include ruby markup."""
    # Apply ruby markup
    return add_ruby_markup(verse_text)

